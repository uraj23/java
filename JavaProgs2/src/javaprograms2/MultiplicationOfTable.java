package javaprograms2;


public class MultiplicationOfTable 
{
	public static void main(String[] args) 
	{
		//multiplication of 11 to 20 tables
		int num=11;
		int num1=20;
		for(int i=num;i<=num1;i++)
		{
			for(int j=1;j<=10;j++)
			{
				System.out.println(i+"*"+j+"="+i*j);
			}
		}
		
		
		
	}

}
